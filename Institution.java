    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        try
       {
           
        String sql = "Update `orphanagesystem`.`institution` set `id` = ?, `name` = ?, `address` = ?,`contactno` = ? where `id` = ? ";
        ps = con.prepareStatement(sql);
        ps.setInt(1,parseInt(id.getText()));
        ps.setString(2,name.getText());
        ps.setString(3,address.getText());
        ps.setString(4, contactNo.getText());
        ps.setInt(5,parseInt(id.getText()));
        ps.execute();
        JOptionPane.showMessageDialog(null,"Inserted");
       }
       catch (SQLException e)
       {
           JOptionPane.showMessageDialog(null,e);
       }
    }//GEN-LAST:event_updateButtonActionPerformed

