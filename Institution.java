    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        try
        {
            int sid = parseInt(JOptionPane.showInputDialog("Enter Reg ID to Search : "));
            String sql = "Select * from orphanagesystem.institution where id  = ?";
            ps = con.prepareStatement(sql);
            ps.setInt(1,sid);
            rs = ps.executeQuery();
             DefaultTableModel tm =(DefaultTableModel)searchTable.getModel();
             tm.setRowCount(0);
           
            while(rs.next())
            { 
                
                Object o[] = {rs.getInt("id"),rs.getString("name"),rs.getString("address"),rs.getString("contactno")};
                tm.addRow(o);
            }
             }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_searchActionPerformed

